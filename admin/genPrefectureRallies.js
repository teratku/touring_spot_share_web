// 端点に到達困難点を含み、ビルダーでの差し替えを促すべき県
const FLAG = new Set(["東京都", "島根県", "北海道"]);

function dms(s) {
  const [d, m, sec] = s.split(":").map(Number);
  return +(d + m / 60 + sec / 3600).toFixed(6);
}
const DIRNAME = { n: "最北端", e: "最東端", s: "最南端", w: "最西端" };

function buildTargets(pref, c) {
  // c = {E:[lng,lat],W:[...],S:[...],N:[...]} 十進
  const base = {
    n: { lat: c.N[1], lng: c.N[0] },
    e: { lat: c.E[1], lng: c.E[0] },
    s: { lat: c.S[1], lng: c.S[0] },
    w: { lat: c.W[1], lng: c.W[0] },
  };
  const ov = OVERRIDES[pref] || {};
  const order = ["n", "e", "s", "w"];
  return order.map((dir, i) => {
    const o = ov[dir];
    return {
      targetId: dir,
      name: o ? o.name : `${pref}${DIRNAME[dir]}`,
      lat: o ? o.lat : base[dir].lat,
      lng: o ? o.lng : base[dir].lng,
      address: pref,
      order: i + 1,
    };
  });
}

function main() {
  fs.mkdirSync(OUT_DIR, { recursive: true });
  const rows = CSV.split("\n").map((l) => l.split(","));
  let count = 0;
  const flagged = [];
  for (const r of rows) {
    const pref = r[0];
    const romaji = ROMAJI[pref];
    if (!romaji) { console.warn("romaji未定義:", pref); continue; }
    const c = {
      E: [dms(r[1]), dms(r[2])],
      W: [dms(r[3]), dms(r[4])],
      S: [dms(r[5]), dms(r[6])],
      N: [dms(r[7]), dms(r[8])],
    };
    const targets = buildTargets(pref, c);
    const note =
      `県別四端ラリー（${pref}）。座標は国土地理院由来の“真の端点”を十進変換したもの。` +
      (FLAG.has(pref)
        ? "【要差し替え】離島/岩礁/係争地など到達困難な点を含む。ビルダーで到達可能点へ修正のこと。"
        : "到達性は本番前に要確認（離島はフェリー要の場合あり）。") +
      "targetId(n/e/s/w)は固定。";
    const rally = {
      _note: note,
      rallyId: `pref-${romaji}-extreme-${FY}`,
      name: `${pref} 東西南北端 ${FY}`,
      theme: "extreme",
      category: "prefecture",
      prefecture: pref,
      region: REGION[pref] || "",
      description: `${pref}の東西南北の端をめぐる通年チャレンジ。`,
      fiscalYear: FY,
      startAt: `${FY}-04-01T00:00:00+09:00`,
      endAt: `${FY + 1}-03-31T23:59:59+09:00`,
      rewardBadgeId: `badge_pref_${romaji}_extreme_${FY}`,
      completionTitle: `${pref} 四端制覇 ${FY}`,
      targets,
    };
    fs.writeFileSync(path.join(OUT_DIR, `${romaji}-extreme-${FY}.json`), JSON.stringify(rally, null, 2) + "\n");
    count++;
    if (FLAG.has(pref)) flagged.push(pref);
  }
  console.log(`✅ 生成 ${count} 件 → ${path.relative(__dirname, OUT_DIR)}/`);
  console.log(`⚠️ 到達困難点を含み要差し替え: ${flagged.join("、")}`);
  console.log("   （北海道は宗谷岬/納沙布岬に自動代替済み。東京/島根はビルダーで修正推奨）");
  console.log("投入（アプリのカテゴリ対応後）:");
  console.log(`   for f in generated/prefecture-extreme-${FY}/*.json; do node importRallies.js --file "$f"; done`);
}
main();
