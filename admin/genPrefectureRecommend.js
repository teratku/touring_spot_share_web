function main() {
  fs.mkdirSync(OUT_DIR, { recursive: true });
  let count = 0;
  for (const pref of Object.keys(SPOTS)) {
    const romaji = ROMAJI[pref];
    if (!romaji) { console.warn("romaji未定義:", pref); continue; }
    const targets = SPOTS[pref].map((s, i) => ({
      targetId: `t${String(i + 1).padStart(2, "0")}`,
      name: s[0],
      lat: s[1],
      lng: s[2],
      address: pref,
      order: i + 1,
    }));
    const rally = {
      _note: `県別おすすめラリー（${pref}）。著名観光地を選定。座標は概算のため本番前にビルダーで要確認。targetIdは固定。`,
      rallyId: `pref-${romaji}-recommend-${FY}`,
      name: `${pref} おすすめスポット ${FY}`,
      theme: "recommend",
      category: "prefecture",
      prefecture: pref,
      region: REGION[pref] || "",
      description: `${pref}の人気観光スポットをめぐる通年チャレンジ。`,
      fiscalYear: FY,
      startAt: `${FY}-04-01T00:00:00+09:00`,
      endAt: `${FY + 1}-03-31T23:59:59+09:00`,
      rewardBadgeId: `badge_pref_${romaji}_recommend_${FY}`,
      completionTitle: `${pref} 名所めぐり ${FY}`,
      targets,
    };
    fs.writeFileSync(path.join(OUT_DIR, `${romaji}-recommend-${FY}.json`), JSON.stringify(rally, null, 2) + "\n");
    count++;
  }
  const missing = Object.keys(ROMAJI).filter((p) => !SPOTS[p]);
  console.log(`✅ 生成 ${count} 件 → ${path.relative(__dirname, OUT_DIR)}/`);
  if (missing.length) console.log("⚠️ SPOTS未定義:", missing.join("、"));
  console.log("⚠️ 座標は概算。本番前にビルダー（既存ラリー読込→地図で微修正→保存）で要確認。");
  console.log(`投入: for f in generated/prefecture-recommend-${FY}/*.json; do node importRallies.js --file "$f"; done`);
}
main();
